# strings

course = "Python Programming"

# len(): get the length of a string
print(len(course))

# []: to access specific element or specific character
# : : to slice a string
print(course[0])
print(course[-1])
print(course[0:3])
print(course[0:])
print(course[:3])
print(course[:])


