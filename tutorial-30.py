# VSCode Coding Tricks - KEYBOARD SHORTCUTS

# press END key to move the cursor to the end of the lines
# press HOME key to move the cursor to the beginning of the lines
# press CONTROL + HOME key to move the cursor to the beginning of the files
# press CONTROL + END key to move the cursor to the end of the files
# press ALT + UP arrow key to move the specific line (one or more lines) up
# press ALT + DOWN arrow key to move the specific line (one or more lines) down
# Hold SHIFT + ALT and press DOWN arrow key to duplicate a line or multiple lines
# press CONTROL + / to comment out the specific lines and remove the comment
# autocomplete the variables when typing first two characters and press ENTER key
# pick any letters in sequence and type the variables


def multiply(*numbers):
    total = 1
    for number in numbers:
        total *= number
    return total


print("Start")
print(multiply(1, 2, 3))
